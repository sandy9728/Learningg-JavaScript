//  **** while loop **** //

let index = 0   
while (index <= 10) {
    // console.log(`Value of index is ${index}`);
    index = index + 2

}


let myArr = ["Ironman", "Superman", "Batman", "Spiderman"]

let arr = 0
while (arr < myArr.length) {
    // console.log(`Value is ${myArr[arr]}`);
    arr = arr + 1
    
}

// **** Do while loop **** //

let Value = 1
do {
    // console.log(`value is ${Value}`);
    Value++
    
} while (Value <= 10);


let values = 11 // This still works because the condition is checked after the code is executed
