const month = 2

switch (month) {
    case 1:
        console.log("Janurary");
        break;
    case 2:
        console.log("Feburary");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;

    default:
        console.log("Default case matched");
        break;
}